# for ლუპით დაუარეთ რიცხვებს 0 დან 100 ჩათვლით, თუ რიცხვი(i) კენტია დაბეჭდეთ 'კენტია' თუ რიცხვი(i)ლუწია დაბეჭდეთ 'ლუწია'. თუ რიცხვი(i) ნულია დაბეჭდეთ 'ნული'
for i in range(101):
    if i % 2 == 1:
        print('კენტია')
    elif i % 2 == 0:
        print('ლუწია')
    elif i == 0:
        print('ნული')




